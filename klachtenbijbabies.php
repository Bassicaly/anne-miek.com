Wanneer naar een osteopaat
Veel voorkomende klachten bij baby’s
Tijdens een bevalling worden grote krachten uitgeoefend op het lichaam van het ongeboren kind. De beweeglijkheid van het lichaampje en de schedel zorgen er in het algemeen voor dat deze krachten goed opgevangen kunnen worden. Maar als de bevalling erg lang heeft geduurd, of als deze juist heel snel is gegaan, dan kan dat gevolgen hebben voor het welzijn van het kind. Ook kunnen klachten ontstaan na een geboorte van een baby in stuitligging, na een vacuümverlossing of een keizersnede.
Een osteopaat kan dan op een zachte manier het lichaam van de baby doen ontspannen en zijn natuurlijke beweeglijkheid teruggeven. Er wordt beslist géén manipulaties uitgevoerd.
Het is onmogelijk een complete lijst te maken van alle klachten waarmee u met uw baby bij een osteopaat terecht kan. Hieronder vindt u een lijst van de klachten die osteopaten het meest tegenkomen. Staat de klacht van uw kind er niet bij, aarzel dan niet contact op te nemen met een erkend osteopaat en vraag of hij of zij iets voor u kan betekenen.
