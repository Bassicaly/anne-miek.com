Wanneer naar een osteopaat
Veel mensen met chronische rug- of nekpijn hebben baat bij behandeling door een osteopaat. Maar de lijst van klachten waarbij een osteopaat kan helpen is nog veel langer. Ook bij bijvoorbeeld migraine, whiplash, buikpijn, slechte darmwerking kan behandeling door een osteopaat zinvol zijn. Ook is het belangrijk om te weten dat osteopathie uitkomst kan bieden bij huilbaby’s.
Het is onmogelijk een lijst te maken van alle klachten waarmee u bij een osteopaat terecht kunt. Hieronder vindt u een lijst van de klachten die osteopaten het meest tegenkomen. Staat uw klacht er niet bij, aarzel dan niet contact op te nemen met een erkend osteopaat en vraag of hij of zij iets voor u kan betekenen.
•	Pijnlijke of stramme ledematen, rug, nek, schouder en heup
•	Bekkenklachten, ook gerelateerd aan de zwangerschap
•	Sportblessures, verstuikingen
•	Whiplash
•	Hoofdpijn
•	Terugkerende bijholteontstekingen of oorontstekingen
•	RSI
•	Refluxproblemen
•	Incontinentie
•	Maag- of darmklachten
•	Pijn na operaties
•	Menstruatiestoornissen
•	Oedeem
•	Concentratieproblemen
•	Chronische vermoeidheid
Een osteopaat onderzoekt en behandelt altijd het gehele lichaam. Als u meer wil weten over de behandeling, klik danhier.
Osteopaten werken samen met reguliere geneeskundigen. Dankzij hun opleiding herkennen osteopaten klachten die nader onderzoek vereisen door bijvoorbeeld de huisarts of de specialist. Als het nodig is, verwijst de osteopaat u naar hen door.
